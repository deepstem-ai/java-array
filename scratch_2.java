class Main {
    public static void main(String[] args) {

        // create an array
        int[] age = {12, 4, 5, 152, 954, 895, 512, 654, 1045};

        // loop through the array
        // using for loop
        System.out.println("Using for-each Loop:");
        for(int i : age) {
            System.out.println(i);
        }
    }
}